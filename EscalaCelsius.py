# Entrada de dados
print("Programa para converter temperatura de Fahrenheit para Celsius")
print("-------------------------------------------------------------")
print("Digite a temperatura em Fahrenheit: ")
F = float(input())

# Processamento de dados
C = 5/9*(F - 32)

# Saída de dados
print("-------------------------------------------------------------")
print(f"A temperatura em Celsius é: {C} °C")
print("-------------------------------------------------------------")
print()

